package javatest;

public class main {
    public static void main(String[] args) {
        // primitive data type
        int helloWorld = 5;
        double num2 = 5.5;
        boolean b = true;
        char c = 'X';

        // non-primitive data type
        String a = "1b%";

        System.out.println(helloWorld);
        System.out.println(num2);
        System.out.println(b);
        System.out.println(c);
        System.out.println(a);
}
}